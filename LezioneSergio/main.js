/* Fare un calcolatrice tra due numeri con addizione, sottrazione, moltiplicazione */

function addizione(a,b){
    return a+b;
} 
function sottrazione(a,b){
    return a-b;
}
function moltiplicazione(a,b){
    return a*b;
}

console.log(addizione(2,3))
console.log(sottrazione(2,3))
console.log(moltiplicazione(2,3))
